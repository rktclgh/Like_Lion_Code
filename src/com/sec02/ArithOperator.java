package com.sec02;

public class ArithOperator {
	public static void main(String[] args) {

		int result = 0;
		int value01 = 50;
		int value02 = 20;

		result = value01 + value02;
		System.out.println(value01 + "+ " + value02 + " =" + result);

		result = value01 - value02;
		System.out.println(value01 + "- " + value02 + " =" + result);

		result = value01 * value02;
		System.out.println(value01 + "* " + value02 + " =" + result);

		result = value01 / value02;
		System.out.println(value01 + "/ " + value02 + " =" + result);

		result = value01 % value02;
		System.out.println(value01 + "% " + value02 + " =" + result);

	}

}
